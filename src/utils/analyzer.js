function mockAnalyze(answers) {
  const fullText = answers.join(' ')
  const scores = { creative: 0, analytical: 0, social: 0, nature: 0, entrepreneur: 0 }

  for (const [type, keywords] of Object.entries(KEYWORDS)) {
    for (const keyword of keywords) {
      const matches = fullText.match(new RegExp(keyword, 'gi'))
      if (matches) scores[type] += matches.length
    }
  }

  let dominant = 'creative'
  let max = -1
  for (const [type, score] of Object.entries(scores)) {
    if (score > max) { max = score; dominant = type }
  }

  return PERSONALITY_TYPES[dominant]
}

export async function analyzeUserInput(answers) {
  try {
    const base = import.meta.env.DEV ? 'http://localhost:3001' : ''
    const res = await fetch(`${base}/analyze`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ answers }),
    })

    if (!res.ok) throw new Error(`서버 오류 (${res.status})`)

    return await res.json()
  } catch {
    return mockAnalyze(answers)
  }
}
